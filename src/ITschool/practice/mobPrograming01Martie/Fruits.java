package ITschool.practice.mobPrograming01Martie;

public class Fruits {

    private int apples;
    private int bananas;

    private double priceApples;
    private double priceBananas;

    public double getPriceApples() {
        return priceApples;
    }

    public void setPriceApples(double priceApples) {
        this.priceApples = priceApples;
    }

    public double getPriceBananas() {
        return priceBananas;
    }

    public void setPriceBananas(double priceBananas) {
        this.priceBananas = priceBananas;
    }

    public int getApples() {
        return apples;
    }

    public void setApples(int apples) {
        this.apples = apples;
    }

    public int getBananas() {
        return bananas;
    }

    public void setBananas(int bananas) {
        this.bananas = bananas;
    }
}
